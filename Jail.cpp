void Game::jail()
{
    /*
    This function will move a player directly to jail. It will then set a boolian to true which will remain true until three turns
    pass or the player succeeds in rolling a double. 
    */
    string playerPiece = player[currentTurn - 1].getPlayerChar();
    setPiece(10,playerPiece, currentTurn);
    player[currentTurn - 1].setBoardLocation(10);
    erase(currentTurn);
    display_MapAndPlayer();
    player[currentTurn - 1].setJailCounter(3);
    bool jail = true;
    player[currentTurn - 1].setJailStatus(jail);
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool Game::getInJail()
{
    return player[currentTurn - 1].getJailStatus();
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Game::setInJail(bool status)
{
    player[currentTurn - 1].setJailStatus(status);
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Game::setTurnsInJail(int number)
{
    player[currentTurn - 1].setJailCounter(number);
}